      <h4>How Laser Tattoo Removal Works</h4>
      <div class="spacer-sm"></div>
      <p>
        Laser tattoo removal works through a process known as selective photothermolysis. This process targets the tattoo ink with highly concentrated laser beams of specific wavelengths that match the ink colours for optimal absorption. Here's a step-by-step explanation of how it works:
        </p>
        </br>
        <div class="list-with-image">
            <ul>
                <li>Targeting the Ink: When the laser is applied to the skin, its light is specifically absorbed by the tattoo ink particles due to the wavelength of the laser light being selectively absorbed by the colours of the tattoo ink. The surrounding skin tissue, not being the target, absorbs much less of this energy and is therefore not damaged.</li>
                <li>Ink Particle Breakdown: The absorbed energy causes the tattoo ink particles to heat up and shatter into smaller fragments. The breakdown of these particles is critical because the original ink particles are too large for the body to remove naturally.</li>
                <li>Body’s Immune Response: Once the ink particles are broken down into smaller pieces, the body's immune system recognises these fragments as foreign objects. White blood cells then move in to engulf and gradually remove these particles from the site through the body's lymphatic system.</li>
                <li>Gradual Fading of the Tattoo: This process doesn't happen overnight. Each laser treatment breaks down more ink particles, and the tattoo gradually fades over a series of treatments. The number of sessions needed varies depending on the tattoo's size, color, depth, and the type of ink used, as well as the individual's skin type and their body's response to the treatment.</li>
                <li>Multiple Sessions for Effectiveness: Multiple laser treatments are typically necessary to achieve significant fading, with sessions spaced several weeks apart. This spacing allows the skin to heal and the body to flush away ink particles.</li>
                <li>Wavelength and Pulse Duration: Different wavelengths of laser light are used to target different ink colors effectively, as different colours absorb different wavelengths. Pulse durations, measured in nanoseconds (billionths of a second) or picoseconds (trillionths of a second), are also crucial. Shorter pulses such as those from picosecond lasers can shatter ink particles more effectively than longer pulse lasers.</li>
                <li>Selective Absorption: It's worth noting that not all colours are equally easy to remove. Black and dark blue inks are the easiest to treat because they absorb a wide range of laser wavelengths. Bright colours like green, yellow, and fluorescent inks can be more challenging and may require lasers with specific wavelengths to target them effectively.</li>
            </ul>
